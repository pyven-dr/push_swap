#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*mem;

	i = 0;
	mem = s;
	while (i < n)
	{
		mem[i] = 0;
		i++;
	}
	return (s);
}
